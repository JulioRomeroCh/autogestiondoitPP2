package logicadenegocios;

public interface Comparable {
  /**
   * <p> Método que compara dos objetos de tipo Comparable.
   * @param pObjeto: Objeto de tipo Comparable.
   * @return boolean que representa si un objeto es menor o mayor a otro, según sea el criterio.
   */  
  public abstract boolean comparar(Comparable pObjeto);  
     
}


